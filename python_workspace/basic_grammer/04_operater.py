# 산술 연산자 : +, -, *, /, %
x=10
y=20
print("x+y = ", x+y , end='\n')
print("x-y = ", x-y , end='\n')
print("x*y = ", x*y , end='\n')
print("x/y = ", x/y , end='\n')
print("x%y = ", x%y , end='\n')
print("2**2 = ", 2**2, end='\n')
